import { ItemDetailsComponent } from '../item-details/item-details.component';
import { Item } from 'src/app/models/Item';
import { ItemType } from 'src/app/models/ItemType';

export interface UserData {
  id: string;
  name: string;
  progress: string;
  fruit: string;
}

